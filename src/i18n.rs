pub fn detect(accept_language: &str) -> String {
    for part in accept_language.split(',') {
        let lang = part.trim().split(';').next().unwrap_or("").split('-').next().unwrap_or("");
        if lang == "zh" {
            return "zh".to_string();
        }
    }
    "en".to_string()
}
